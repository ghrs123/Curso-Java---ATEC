package atec.flj.Mat;

public class NumeroInt {
	private int numero;

	public NumeroInt(int numero) {
		this.numero = numero;

	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public String toString() {
		return String.valueOf(numero);
	}

}
